'''
dias de la semana

'''

dias=int=(input("ingrese un numero del dia del 1 al 7: "))

if dias==1:
    print("Domingo")
elif dias==2:
    print("Lunes")
elif dias==3:
    print("Martes")
elif dias==4:
    print("Miercoles")
elif dias==5:
    print("Jueves")
elif dias==6:
    print("Viernes")
elif dias==7:
    print("Sabado")
else:
    print("numero incorrecto")